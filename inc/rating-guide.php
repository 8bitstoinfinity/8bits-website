        <h2>Rating Guide</h2>
        <p>
                Everyone who contributes to one of our game jams is able to
                judge games on the following five criteria, rating from one to
                five stars. You are welcome to judge these criteria as you wish,
                but the following extensive guide allows for more consistent
                ratings.
        </p>

        <h3>Categories</h3>
        <ul>
                <li>
                        <a href="#fundesign">Fun/Design</a>
                </li>
                <li>
                        <a href="#technical">Technical Implementation</a>
                </li>
                <li>
                        <a href="#audio">Music/Sound</a>
                </li>
                <li>
                        <a href="#graphics">Graphics/Animation</a>
                </li>
                <li>
                        <a href="#theme">Theme/Limitation</a>
                </li>
        </ul>

        <h3>General</h3>
        <p>
                Remember that a three-star rating is average. Prefer rating 2,
                3, or 4 stars unless the game is notably awful (1 star) or
                excellent (5 stars) in the category.
        </p>
        <p>
                If a category does not apply to the game, use the closest analogue in
                the game's environment. For instance, Graphics/Animation in a
                text-only game could be interpreted as imagery, style, tone,
                etc. If you are still unsure, rate a 3 (average) in the category.
        </p>
        <p>
                Each category below includes a list describing what each number
                of stars should represent. The following applies to all
                categories:
        </p>
        <ol>
                <li>Minimal or zero effort.</li>
                <li>Less than average effort, lacking skill or knowledge</li>
                <li>About average for a game jam game; nothing special</li>
                <li>Clear extra effort; higher quality than normal</li>
                <li>Essentially flawless with significant effort shown</li>
        </ol>

        <a class="anchor" id="fundesign"></a>
        <h3>Fun / Design</h3>
        <p>
                Enjoyment of the game. Are the mechanics interesting? Do you want to play more?
        </p>
        <ol>
                <li>Clear rehash of a known idea, or difficult to stomach one playthrough</li>
                <li>One time was enough, or a well-known mechanic with few changes</li>
                <li>Nothing special, a standard twist on an existing mechanic or multiple combined mechanics</li>
                <li>More fun than average, with some effort shown to create a new idea, even if derivative</li>
                <li>So fun you had to force yourself to stop, with a super-clever twist or unusual design</li>
        </ol>

        <a class="anchor" id="technical"></a>
        <h3>Technical Implementation</h3>
        <p>
                Polish and complexity. Does the game push technical boundaries
                or have special tech to stand out? Are there few, if any, bugs
                and typos?
        </p>
        <ol>
                <li>Several problematic bugs and technical issues</li>
                <li>A few bugs, but few of them cause major problems</li>
                <li>Average technical implementation. Nothing special, but most things work</li>
                <li>Some stand-out technical pieces such as database access or an unusual input</li>
                <li>Impressive technical implementation such as networking or Twitch integration</li>
        </ol>

        <a class="anchor" id="audio"></a>
        <h3>Music / Sound</h3>
        <p>
                Audio presentation. How well does the music/sound fit the game?
        </p>
        <p>
                If the game is primarily narrative text with no audio, replace
                this with tone and language. Does the text flow smoothly? Is
                dialogue so realistic you can hear it?
        </p>
        <ol>
                <li>No audio, or very poor implementation</li>
                <li>Music or sounds missing, or they're inappropriate or jarring</li>
                <li>Enough music and sounds to get by, but average quality</li>
                <li>Either music or sounds are stand-out quality and fit the game well</li>
                <li>Dynamic, appropriate, high-quality audio with music and all necessary sounds</li>
        </ol>

        <a class="anchor" id="graphics"></a>
        <h3>Graphics / Animation</h3>
        <p>
                Visual presentation. How clean/consistent does it look? Are animations fluid? Is there clear understanding of color, contrast, and other visual skills? Do the graphics contribute to the game's playability?
        </p>
        <p>
                If the game is primarily narrative text with no graphics, replace this with imagery. Are the characters, situations, and setting vivid?
        </p>
        <ol>
                <li>Poor composition and minimal or no animation</li>
                <li>Missing animations but visually okay, or animated but visually subpar</li>
                <li>Graphics fit the game with some animations at average quality</li>
                <li>Several animations or demonstration of higher art skills</li>
                <li>Brilliant use of color, shape, and animation, well-fit to the game</li>
        </ol>

        <a class="anchor" id="theme"></a>
        <h3>Theme/Limitation</h3>
        <p>
                Implementation of jam theme and limitation. Is the theme
                prevalent? How unusual is the interpretation? How strictly does
                the game follow the requirement? Out-of-the-box thinking on the
                theme (not taking it literally) should score more points.
        </p>
        <p>
                Remember that the limitation is <em>required</em> to qualify for
                the jam, so if you believe a game does nothing to meet the
                limitation, please report it instead of rating.
        </p>
        <ol>
                <li>Limitation is there, but theme isn't</li>
                <li>Theme and limitation are there, but you have to stretch your imagination to see one or the other (or both)</li>
                <li>Theme and limitation integrated with the game story or mechanics</li>
                <li>Theme and limitation integrated with game story and mechanics, or used in a clever way</li>
                <li>Theme and limitation integrated with both game story and mechanics, <em>and</em> used in a clever way</li>
        </ol>
