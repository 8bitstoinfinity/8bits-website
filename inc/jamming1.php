        <h2 id='title'>
                Article: What Is a Game Jam?
        </h2>
        <div id='byline'>
                By <a href='https://www.mrjoshuamclean.com'>Joshua McLean</a>
        </div>
        <p><em>Would you prefer to listen to the post? <a href="media/game-jam-success-1.mp3">Download a recording here!</a></em></p>
        <p>
                Game development is time-consuming, complicated, and messy. The
                biggest titles involve large teams, gigantic budgets, and an
                inconceivable number of working hours. It's an industry fueled
                by blood, sweat, and tears.
        </p>
        <p>
                However, thousands of games are created every year by solo developers or
                small teams on zero budget over a weekend or week. Some of these become
                full-fledged commercial projects and find success in the market. You may
                have heard of some: <em>Goat Simulator</em>, <em>Super Hot</em>, <em>Celeste</em>, <em>Hollow
                        Knight</em>, and <em>Surgeon Simulator</em>, to name a few.</p>
        <p>
                How does this happen? Through game jams! These time-limited events
                challenge participants, whether solo or on a team, to create brand-new
                games with a theme and/or limitations. Some major global jams include
                <a href="http://ldjam.com/">Ludum Dare</a> and <a href="https://globalgamejam.org/">Global Game
                        Jam</a>, but anyone can create and host their
                own jam on sites such as <a href="https://itch.io/jams">itch.io</a>, where jams
                range from a dozen entrants to over a thousand.
        </p>
        <p>
                Game jams encourage creative designs by utilizing a key part of game
                development: rapid prototyping. Sure, it may take years to create a
                game, but that core idea - what we call the <em>core mechanic</em> - can be
                developed in only a few hours.
        </p>
        <p>
                They also promote community, encouragement, and creativity, and offer a
                place for new developers to practice game development without dedicating
                years to a single project. With a community focus, game jams are also a
                fantastic networking opportunity, allowing developers to meet and team
                up with fellow creatives.
        </p>
        <h3>
                Concerns and Misconceptions
        </h3>
        <p>
                Many people new to game jams (or to game development) find the idea
                overwhelming. There are many misconceptions surrounding the events.
                Let's look at some of those.
        </p>
        <p>
                <strong>(1) The goal of a game jam is to create a full game in the time
                        limit.</strong>
        </p>
        <p>
                On the contrary: Game jams are about creating a functional <em>prototype</em>.
                This could demonstrate a new core mechanic or a twist on an old
                favorite, or even provide a small experience with a targeted emotion or
                response. Somewhere, corners will be cut. The game might lack polish (in
                areas like graphics, control, and audio), last only a few minutes, or
                show only one side of a mechanic rife with possibilities.
        </p>
        <p>
                Finished games require far more polish than a game jam time limitation
                will allow. Proper polish often requires outside perspectives, thorough
                testing, and a bit of reflection time from the creators, all of which
                add the need for more time.
        </p>
        <p>
                <strong>(2) If my game jam prototype is bad, I wasted my entire
                        weekend/week.</strong>
        </p>
        <p>
                Game jams are a learning opportunity, not a test. Each time you
                participate in a jam, you learn more about yourself and about game
                development. Don't try to &quot;win&quot; or make a &quot;perfect&quot; game. Instead, try
                to push and improve yourself as a game developer.
        </p>
        <p>
                Failure is good. Learn from your mistakes and make the next one better.
                One of the best pieces of advice in game development is to <em>fail
                faster</em>, and game jams embrace this philosophy.
        </p>
        <p>
                Not only that, but you'll have <em>something that you made</em> at the end.
                Whether it's the full game or simply pieces like music, art, sound,
                design, or ideas, you've brought a new thing into the world which you
                can share, shape, and drop into your portfolio. Game jams demonstrate
                your skills, and this can be vital to landing a job in the industry,
                especially on the indie side.
        </p>
        <p>
                <strong>(3) Crunch is bad, so I shouldn't participate in game jams.</strong>
        </p>
        <p>
                This is an unfortunate misconception about game jams. Whether you're
                making a game in two days or two years, it's up to you (or the people in
                charge, if you're on a team or part of a company) whether crunch is
                involved.
        </p>
        <p>
                Crunch is when you push yourself extra hard by cutting out &quot;down time&quot; -
                time for sleep, food, relaxing, spending time with family and friends,
                etc. - to get work done more quickly. This is an awful practice. Sure,
                it's important to dedicate as much time as you can to work, but these
                other parts of life are just as important, and the &quot;recharge&quot; they
                provide can improve your work.
        </p>
        <p>
                The time limitation for a jam is not meant to encourage crunch
                mentality. Rather, it's to keep the event on a small scale to learn
                about scoping and improve skills in a short period of time.
        </p>
        <p>
                Shorter time periods are easier to scope. You can approximate how many
                hours you're going to spend on code, art, and audio within a 48-hour
                period much better than over six months. It's also easier to move from
                the small scale to the larger one, generalizing how much you get done in
                a (non-crunch) 48-hour period and applying it to a six-month period.
        </p>
        <p>
                When participating in a game jam, no matter the length, take care of
                yourself. Eat and sleep as you normally would, and use the work hours
                you have available. There is no need to stay up for 48 hours straight of
                work, which will hurt the project more than help it. If the jam is a
                week long, either dedicate a few specific days to the jam, or block out
                a few hours every day - whatever allows you to work in relative comfort.
        </p>
        <p>
                <strong>(4) A game jam is a waste of time that could be spent on a larger
                project.</strong>
        </p>
        <p>
                Even if you are working on a larger project, the idea of prototyping -
                what game jams are all about - still applies. Before embarking on a game
                that will take six months or longer to develop, you want to make sure
                the basic idea is good, or at least workable. Prototyping is a great way
                to do this. Game jams offer an ideal environment for prototyping. You
                work along with others, meaning everyone can help each other, and in the
                end you get feedback, which is critical to determining if a prototype
                will work.
        </p>
        <p>
                So yes, maybe your end goal is to work on a larger project, but a game
                jam can help push it along faster on a successful path.
        </p>
        <p>
                <strong>(5) Game jams are hard.</strong>
        </p>
        <p>
                Okay, that one's accurate. But it's not just game <em>jams</em> that are hard.
        </p>
        <p>
                Game development <em>in general</em> is difficult. Many creative and technical
                skills come together to create a game. Even if you excel at design,
                programming, audio, and art, there is <em>hard work</em> in the amount of time
                required. Games are a complex creative product, often tracking back
                during development and heading in multiple directions at once.
        </p>
        <p>
                Jams, however, provide an opportunity to practice the needed skills.
                These experiences can help narrow your focus. Perhaps you want to be
                involved in games, but only on audio, or art, or programming. The only
                way to know is by trying.
        </p>
        <p>
                Even better, you can also practice &quot;invisible&quot; skills involved in game
                creation, such as scoping, testing, polishing, refinement, branding,
                publishing, marketing, giving/receiving feedback, and so on.
        </p>
